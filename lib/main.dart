import 'package:english_words/english_words.dart';
import 'package:flutter/material.dart';
import 'package:provider/provider.dart';

// Point d'entree du programme
void main() {
  runApp(MyApp());
}

// Tout est widget et MyApp est le widget qui appel et configure tous les autres widget, le nom de l'app un peu comme le setting
// Ici il dit que le widget principal, celui qui s'affiche quand on lance l'app c'est MyHomePage (voir ligne 26)

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return ChangeNotifierProvider(
      create: (context) => MyAppState(),
      child: MaterialApp(
        title: 'Namer App',
        theme: ThemeData(
          useMaterial3: true,
          colorScheme: ColorScheme.fromSeed(seedColor: Colors.deepOrange),
        ),
        home: MyHomePage(),
      ),
    );
  }
}

// MyAppState détermine les données dont l'application a besoin pour fonctionner.

class MyAppState extends ChangeNotifier {
  
  var current = WordPair.random();
  var favorites = <WordPair>[];

  void getNext() {
    current = WordPair.random();
    notifyListeners();
  }

  void toggleFavorite() {
    if (favorites.contains(current)) {
      favorites.remove(current);
    } else {
      favorites.add(current);
    }
    notifyListeners();
  }

}


